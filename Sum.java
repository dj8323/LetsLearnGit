public class Sum {
    public static void main(String[] args) {
        // Define two numbers to add
        int num1 = 10;
        int num2 = 20;

        // Add the two numbers
        int sum = num1 + num2;
        System.out.println("The sum of " + num1 + " and " + num2 + " is: " + sum);
    }
}

